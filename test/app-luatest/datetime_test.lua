local pg = t.group('pgroup')

-- It is not possible to use parameterization by passing a table
-- with test parameters to `t.group` because datetime format
-- strings in test parameters contains symbol `/` that is not
-- allowed in testcases names. Source code below inserts
-- test functions to a test group with testnames where `/` is
-- replaced with `_`.
for _, case in ipairs(rfc3339_iso8601_test_formats) do
    local testcase_name = string.gsub(case.fmt, "/", "_")
    pg['test_rfc3339_formats_' .. testcase_name] = function()
        t.skip_if(not case.is_supported_rfc3339, 'not supported by RFC 3339')
        local is_parsed, _ = pcall(
            datetime.parse, case.buf, {format = 'rfc3339'})
        t.assert_equals(is_parsed, case.is_parsed_with_rfc3339)
    end

    pg['test_iso8601_formats_' .. testcase_name] = function()
        t.skip_if(not case.is_supported_iso8601, 'not supported by ISO 8601')
        local is_parsed, _ = pcall(
            datetime.parse, case.buf, {format = 'iso8601'})
        t.assert_equals(is_parsed, case.is_parsed_with_iso8601)
    end
end
